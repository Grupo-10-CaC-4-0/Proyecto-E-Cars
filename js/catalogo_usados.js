function filtro (op){
    let filtro1 = ''
    let filtro2 = ''
    if (op){
        filtro1 = document.getElementById('años1').value
        filtro2 = document.getElementById('marcas1').value

        document.getElementById('años1').value = 0
        document.getElementById('marcas1').value = 0     
    }
    else{
        filtro1 = document.getElementById('años2').value
        filtro2 = document.getElementById('marcas2').value

        document.getElementById('años2').value = 0
        document.getElementById('marcas2').value = 0     
    }
    
    if (filtro1 != 0){
        document.getElementById("elegido").innerText = `Ud. eligió Vehiculos del año: ${filtro1}`    
        mostrar(filtro1, 0)
    }
    if (filtro2 != 0){
        document.getElementById("elegido").innerText = `Ud. eligió Vehiculos Marca: ${filtro2}`
        mostrar(0, filtro2)
    }
    if(filtro1 == 0 && filtro2 == 0){
        document.getElementById("elegido").innerText = `Debe seleccionar año o marca.`
        ocultar()
        let vacio = {vehiculos:[]}
        renderVehiculos (vacio)
    }
}

