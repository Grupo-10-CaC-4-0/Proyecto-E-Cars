function mostrar(f1, f2){
    let newElegido = {vehiculos: []}
    for(let i=0; i < datos.vehiculos.length; i++){
        if(datos.vehiculos[i].año == f1 ||datos.vehiculos[i].marca == f2){
            newElegido.vehiculos.push(datos.vehiculos[i])
        }
    }
    if (newElegido.vehiculos.length == 0){
        document.getElementById('elegido2').style = 'display:GRID'
        document.getElementById('elegido2').innerText = 'NO HAY VEHICULOS PARA MOSTRAR'
        let vacio = {vehiculos:[]}
        document.getElementById("ofertas").style = 'display: none'
        document.getElementById("persiana").style = 'display: block'
        renderVehiculos (vacio)
    }
    else{
        ocultar()
        renderVehiculos(newElegido)
    }
}

